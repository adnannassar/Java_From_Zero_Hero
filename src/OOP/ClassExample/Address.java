package OOP.ClassExample;

public class Address {
    String ort;
    String straße;
    int plz;
    int  hausnummer;

    public Address(String ort, String straße, int plz, int hausnummer) {
        this.ort = ort;
        this.straße = straße;
        this.plz = plz;
        this.hausnummer = hausnummer;
    }
}
